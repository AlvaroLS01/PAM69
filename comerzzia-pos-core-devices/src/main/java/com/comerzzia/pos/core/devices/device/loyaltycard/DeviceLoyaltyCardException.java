package com.comerzzia.pos.core.devices.device.loyaltycard;

public class DeviceLoyaltyCardException extends Exception {

	private static final long serialVersionUID = 1L;

	public DeviceLoyaltyCardException() {
		super();
	}

	public DeviceLoyaltyCardException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public DeviceLoyaltyCardException(String message, Throwable cause) {
		super(message, cause);
	}

	public DeviceLoyaltyCardException(String message) {
		super(message);
	}

	public DeviceLoyaltyCardException(Throwable cause) {
		super(cause);
	}

}