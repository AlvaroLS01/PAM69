package com.comerzzia.pos.util.cryptography;

public class CriptoException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -4386088877202220488L;

	public CriptoException() {
	}

	public CriptoException(String arg0) {
		super(arg0);
	}

	public CriptoException(Throwable arg0) {
		super(arg0);
	}

	public CriptoException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
